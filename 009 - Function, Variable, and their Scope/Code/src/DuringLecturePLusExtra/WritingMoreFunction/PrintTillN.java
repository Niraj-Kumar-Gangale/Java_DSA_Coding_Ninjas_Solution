package DuringLecturePLusExtra.WritingMoreFunction;

public class PrintTillN {

    public static void PrintTillNNumber (int n){
        for(int i = 1 ; i<=n ; i++){
            System.out.println(i);
        }
    }

    public static void main(String[] args) {
        PrintTillNNumber(10);
    }
}
