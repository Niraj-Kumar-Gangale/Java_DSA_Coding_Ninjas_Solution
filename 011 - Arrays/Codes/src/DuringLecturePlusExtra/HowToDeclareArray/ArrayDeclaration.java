package DuringLecturePlusExtra.HowToDeclareArray;

public class ArrayDeclaration {
    public static void main(String[] args) {
        //  We will see how  to declare the array of fixed size
        // two ways to declare
        // FIRST

        int[] firstArray ; // only declaration No Space is allocated.
        firstArray = new int[10]; // Here Space Is Allocated.

        // SECOND
        // Declaration and Initialization both in single line.
        int[] secondArray = new int[20];

        char[] thirdArray;
        thirdArray = new char[56];
    }

}
