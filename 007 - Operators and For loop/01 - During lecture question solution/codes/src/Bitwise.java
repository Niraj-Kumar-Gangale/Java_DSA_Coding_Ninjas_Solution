
public class Bitwise {
    public static void main(String[] args) {
        System.out.println(2&3);
        System.out.println(2|3);
        System.out.println(2^3);
        System.out.println(~2);
        System.out.println(-7>>1);//-4
        System.out.println(-7<<1);//-14
        System.out.println(7<<1);// 14
        System.out.println(7>>1);// 3

    }
}
