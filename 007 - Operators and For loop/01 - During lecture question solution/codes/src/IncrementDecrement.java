public class IncrementDecrement {
    public static void main(String[] args) {

        int a = 5;
        System.out.println(a);
        // post increment
        System.out.println(a++);
        a = 5;
        // post decrement
        System.out.println(a--);
        a = 5;
        // pre increment
        System.out.println(++a);
        a = 5;
        // pre decrement
        System.out.println(--a);
    }
}
