package ArithmeticOperatorsAndPrecedence;

public class ArithmeticOperators {
    public static void main(String[] args) {
        /*
        we will talk about the arithmetic operator such as-
        addition +
        subtraction -
        multiplication *
        division /
        modulo i.e remainder %
         */

        // addition
        int add = 45+12;
        System.out.println(add);

        // subtraction
        int sub = 45-4;
        System.out.println(sub);

        // multiplication
        int mul = 15*6;
        System.out.println(mul);

        // division
        int div = 45/2;
        System.out.println(div);
        // answer comes out will be whole number because ((int/int)=int)

        // modulo
        int md = 45%2;
        System.out.println(md);


    }
}
